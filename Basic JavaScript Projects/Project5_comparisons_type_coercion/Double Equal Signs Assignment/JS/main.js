// Use == to perform comparisons and return "true" or "false"
const isEqual1 = 5 == 5; // true
const isEqual2 = 5 == '5'; // true
const isEqual3 = 'hello' == 'world'; // false

console.log(`Is 5 equal to 5?`, isEqual1);
console.log(`Is 5 equal to '5'?`, isEqual2);
console.log(`Is 'hello' equal to 'world'?`, isEqual3);
